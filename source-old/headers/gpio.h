#ifndef GPIO_H
#define GPIO_H

//GPIO0 register address
#define GPIO0_BASE_START 0x44E07000
#define GPIO0_BASE_END 0x44E07FFF
#define GPIO_SIZE GPIO0_BASE_END - GPIO0_BASE_START

//GPIO1 register sddress
#define GPIO1_BASE_START 0x4804C000
#define GPIO1_BASE_END 0x4804CFFF
#define GPIO1_SIZE GPIO1_BASE_END - GPIO1_BASE_START

//GPIO2 register address
#define GPIO2_BASE_START 0x481AC000
#define GPIO2_BASE_END 0x481ACFFF
#define GPIO2_SIZE GPIO2_BASE_END - GPIO2_BASE_START

//GPIO3 register address
#define GPIO3_BASE_START 0x481AE000
#define GPIO3_BASE_END 0x481AEFFF
#define GPIO3_SIZE GPIO3_BASE_END - GPIO3_BASE_START

//GPIO offset address
#define GPIO_REVISION 0x000
#define GPIO_SYSCONFIG 0x010
#define GPIO_EOI 0x020
#define GPIO_IRQSTATUS_RAW_0 0x024
#define GPIO_IRQSTATUS_RAW_1 0x028
#define GPIO_IRQSTATUS_0 0x02C
#define GPIO_IRQSTATUS_1 0x030
#define GPIO_IRQSTATUS_SET_0 0x034
#define GPIO_IRQSTATUS_SET_1 0x038
#define GPIO_IRQSTATUS_CLR_0 0x03C
#define GPIO_IRQSTATUS_CLR_1 0x040
#define GPIO_IRQWAKEN_0 0x044
#define GPIO_IRQWAKEN_1 0x048
#define GPIO_SYSSTATUS 0x114
#define GPIO_CTRL 0x130
#define GPIO_OE 0x134
#define GPIO_DATAIN 0x138
#define GPIO_DATAOUT 0x13C
#define GPIO_LEVELDETECT0 0x140
#define GPIO_LEVELDETECT1 0x144
#define GPIO_RISINGDETECT 0x148
#define GPIO_FALLINGDETECT 0x14C
#define GPIO_DEBOUNCENABLE 0x150
#define GPIO_DEBOUNCINGTIME 0x154
#define GPIO_CLEARDATAOUT 0x190
#define GPIO_SETDATAOUT 0x194

#endif
